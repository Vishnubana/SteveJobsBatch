package com.main;

class ArithmeticOperations {
	
	
	
	void addNum(int a, int b) {
		System.out.println("Addition of "+a+" and "+b+" equals to "+(a+b));
	}
	
	void subNum(int a, int b) {
		System.out.println("Subtraction of "+a+" and "+b+" equals to "+(a-b));
	}
	
	void mulNum(int a, int b) {
		System.out.println("Multiplication of "+a+" and "+b+" equals to "+(a*b));
	}
	
	void divNum(int a, int b) {
		System.out.println("Division of "+a+" and "+b+" equals to "+(a/b));
	}
	
	void modsNum(int a, int b) {
		System.out.println("Modulous of "+a+" and "+b+" equals to "+(a%b));
	}

}
