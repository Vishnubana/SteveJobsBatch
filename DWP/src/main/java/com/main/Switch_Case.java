package com.main;



public class Switch_Case {

	public static void main(String[] args) {
		
		int Lottery_Number=2308045;
		switch(Lottery_Number) {
		case 2308045:{
			System.out.println("'CONGRATULATIONS' you have won the Lottery");
		}
		break;
		case 2308044:{
			System.out.println("'Missed' you have not won the Lottery");
		}
		break;
		case 2308043:{
			System.out.println("'Missed' you have not won the Lottery");
		}
		break;
		case 2308042:{
			System.out.println("'Missed' you have not won the Lottery");
		}
		break;
		case 2308041:{
			System.out.println("'Missed' you have not won the Lottery");
		}
		break;
		default:{
			System.out.println("Please enter a valid input");
		}
		}
	}

}
