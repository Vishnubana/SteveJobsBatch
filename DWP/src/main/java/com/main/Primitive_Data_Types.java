package com.main;

public class Primitive_Data_Types {
	
		public static void main(String [] args){

		int int1=10;
		System.out.println("This is integer 1:"+int1);
		Integer int2=20;
		System.out.println("This is integer 2:"+int2);
		Integer int3=new Integer(30);
		System.out.println("This is integer 3:"+int3);

		byte byte1=2;
		System.out.println("This is byte 1:"+byte1);
		Byte byte2=4;
		System.out.println("This is byte 2:"+byte2);
		Byte byte3=new Byte((byte) 3);
		System.out.println("This is byte 3:"+byte3);

		short short1=3;
		System.out.println("This is short 1:"+short1);
		Short short2=5;
		System.out.println("This is short 2:"+short2);
		Short short3=new Short((short) 2);
		System.out.println("This is short 3:"+short3);

		long long1=11L;
		System.out.println("This is long 1:"+long1);
		Long long2=13L;
		System.out.println("This is long 2:"+long2);
		Long long3=new Long(15L);
		System.out.println("This is long 3:"+long3);

		float float1=12.5f;
		System.out.println("This is float 1:"+float1);
		Float float2=4.5f;
		System.out.println("This is float 2:"+float2);
		Float float3=new Float(16.5f);
		System.out.println("This is float 3:"+float3);

		double double1=11.5;
		System.out.println("This is double 1:"+double1);
		Double double2=13.5;
		System.out.println("This is double 2:"+double2);
		Double double3=new Double(15.5);
		System.out.println("This is double 3:"+double3);

		char char1='a';
		System.out.println("This is char 1:"+char1);
		Character char2='b';
		System.out.println("This is char 2:"+char2);
		Character char3=new Character('c');
		System.out.println("This is char 3:"+char3);

		boolean boolean1=true;
		System.out.println("This is boolean 1:"+boolean1);
		Boolean boolean2=false;
		System.out.println("This is boolean 2:"+boolean2);
		Boolean boolean3=new Boolean(true);
		System.out.println("This is boolean 3:"+boolean3);

		}

		

}
