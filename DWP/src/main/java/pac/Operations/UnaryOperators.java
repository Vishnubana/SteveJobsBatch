package pac.Operations;

public class UnaryOperators {
	
void incrementOperator(int a) {
		
		System.out.println(++a);
	}
	
	void decrementOperator(int a) {
		
		System.out.println(--a);
	}

	void incrementEqualsOperator(int a,int b) {
	
	System.out.println(a+=b);
	}

	void decrementEqualsOperator(int a,int b) {
	
	System.out.println(a-=b);
	}

}
